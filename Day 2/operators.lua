-- Lua uses almost all the operators that other programming languages ​​use, examples:

--[[
  +
  -
  *
  /
  %
  ^
  ==
  ~=
  >
  <
  >=
  <=
  and
  or
  not
  #
  ..
]]

-- Addition
local SUM <const> = 2 + 2
print("2 + 2 =", SUM)

-- Subtraction
local SUBTRACTION <const> = 2 - 2
print("2 - 2 =", SUBTRACTION)

-- Multiplication
local MULTIPLICATION <const> = 2 * 2
print("2 * 2 =", MULTIPLICATION)

-- Division
local DIVISION <const> = 2 / 2
print("2 / 2 =", DIVISION)

-- Module (Remainder of the division)
local MODULE <const> = 2 % 2
print("2 % 2 =", MODULE)

